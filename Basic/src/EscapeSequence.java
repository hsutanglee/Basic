
public class EscapeSequence {

	public static void main(String[] args) {
		System.out.println("Hello World1!");// 
		System.out.print("Hello World1!");// ln means next line
		System.out.print("Hello World1!\n");// \n next line
		System.out.print("Hello World1!\n");// \n next line
		System.out.print("Hello World1!\tHello World1!\n");// \t as tab
		System.out.print("Lee'(Hello World1!)\n");// ' need or no need \
		System.out.print("Lee\"(Hello World1!)\n");// " must use \
		
		
		
		
		
		


	}

}
